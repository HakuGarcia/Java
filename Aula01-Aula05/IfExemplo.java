public class IfExemplo {
    public static void main(String[] args) {

        int idade = 12;

        // Decisão simples
        if (idade == 12) {
            System.out.println();
        }

        // Decisão composta
        if (idade == 10) {
            System.out.println();
        } else
            System.out.println();

        // Decisão encadeada
        if (idade == 8) {
            System.out.println();
        } else if (idade == 9)
            System.out.println();
        else
            System.out.println();

        //Operadores lógicos
        /*
        E - &&
        OU - ||
        Não - !
         */
    }
}
